# Модуль содержит в себе все функции вычисления.

def sum(a, b):
    result = int(a) + int(b)
    return result


def minus(a, b):
    result = int(a) - int(b)
    return result


def mult(a, b):
    result = int(a) * int(b)
    return result


def dev(a, b):
    result = int(a) / int(b)
    return result

